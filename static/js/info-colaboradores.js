//ANA JULIA
$(document).ready(function () {
    $(function () {
        fetch('http://apigsw.herokuapp.com/data/taskcolaborador/anajulia/')
            .then(function (task) {
                return task.json();
            })
            .then(function (dados) {
                $("colab380").append(dados[1]),
                    $("colab381").append(dados[2]);
                $("colab382").append(dados[3]);
                $("colab383").append(dados[5]);
                $("colab384").append(dados[6]);
            });
    });
});

